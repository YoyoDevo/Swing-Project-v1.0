    //method: checkValue
    //purpose: Checks whether or not a user inputs a 1-9 input 
    // for sudoku game
    public void checkValue()
    {
        int check = 0;
      for(int i = 0; i < grid.length; i++)
      {
          if(grid[i].getText().equals(""))
          {
              check = 0;
          }
          else
          {
              check = Integer.parseInt(grid[i].getText());
          }
          
          if(check < 1 || check > 9 )
          {
              sudoErrorMessage.setVisible(true);
          }
      }
    }
    
